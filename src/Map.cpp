Map::Map() {
    platform = TextureManager::LoadTexture("../assets/debugcell.png");
    background = TextureManager::LoadTexture("../assets/water.png");

    dest.x = dest.y = 0;
    dest.w = dest.h = TILE_SIZE;

}


void Map::Draw(bool replace) {

    int type = 0;

    
    for (int row = 0; row < ROWS; row++) {
        for (int column = 0; column < COLUMNS; column++) {

            type = maptiles[row][column];
            
            dest.x = column * dest.w;
            dest.y = row * dest.h;
            

            switch (type) {
                case 0:
                    //TextureManager::Draw(background, src, dest);
                    break;
                case 1:
                    if (replace || GameSpace::framecount == 0) {
                        delete GameSpace::staticcolliders[tilecolnum];
                        GameSpace::staticcolliders[tilecolnum] = new Collider(dest, true);
                    }
                    SDL_RenderCopy(GameSpace::renderer, platform, NULL, GameSpace::camera->transformRect(&dest));
                    
                    tilecolnum++;
                    break;
            }
        }
    }
    tilecolnum = 0;

}